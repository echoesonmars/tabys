function renderTextWithLinks(text: string, links?: Link[]) {
	if (!links || links.length === 0) return <>{text}</>;

	return (
		<>
			{text}
			<div className="mt-2 flex flex-col gap-1">
				{links.map((l) => (
					<a
						key={l.href}
						href={l.href}
						className="text-blue-600 hover:underline break-all">
						{l.label}
					</a>
				))}
			</div>
		</>
	);
}

